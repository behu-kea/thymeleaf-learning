package javaTrainingGround;

public class Wheels {
    public String make;
    public double size;

    public Wheels(String make, double size) {
        this.make = make;
        this.size = size;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public double getSize() {
        return size;
    }

    public void setSize(double size) {
        this.size = size;
    }
}
